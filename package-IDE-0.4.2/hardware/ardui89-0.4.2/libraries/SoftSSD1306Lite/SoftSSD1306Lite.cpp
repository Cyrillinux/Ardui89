// Variables de curseur globales
unsigned char cursorX = 0;
unsigned char cursorY = 0;

void SSD1306_sendCommand(unsigned char cmd) {
    SoftI2cMaster_start(0x78); // Adresse 0x3C << 1
    SoftI2cMaster_write(0x00); // Mode Commande
    SoftI2cMaster_write(cmd);
    SoftI2cMaster_stop();
}

void SSD1306_init(void) {
    SoftI2cMaster_init();
    // Suite de commandes d'init (AE, D5, A8, etc.)
    SSD1306_sendCommand(0xAE); // Écran OFF
    SSD1306_sendCommand(0xD5); SSD1306_sendCommand(0x80); // Diviseur d'horloge
    SSD1306_sendCommand(0xA8); SSD1306_sendCommand(SSD1306_HEIGHT - 1); // Multiplex
    SSD1306_sendCommand(0xD3); SSD1306_sendCommand(0x00); // Décalage d'affichage
    SSD1306_sendCommand(0x40); // Ligne de départ
    SSD1306_sendCommand(0x8D); SSD1306_sendCommand(0x14); // Activer la charge de pompe
    SoftI2cMaster_start(0x78);
    SoftI2cMaster_write(0x00); // Mode Commande
    SSD1306_sendCommand(0x20); SSD1306_sendCommand(0x02); // Mode adressage horizontal
    SSD1306_sendCommand(0xA1); // Remap colonne
    SSD1306_sendCommand(0xC8); // Sortie COM scan direction
    SSD1306_sendCommand(0xDA); SSD1306_sendCommand(0x12); // Config matérielle
    SSD1306_sendCommand(0x81); SSD1306_sendCommand(0xCF); // Contraste
    SSD1306_sendCommand(0xD9); SSD1306_sendCommand(0xF1); // Précharge
    SSD1306_sendCommand(0xDB); SSD1306_sendCommand(0x40); // Vcomh deselect
    SSD1306_sendCommand(0xA4); // Sortie normale
    SSD1306_sendCommand(0xA6); // Mode normal
    SSD1306_sendCommand(0xAF); // Écran ON
}

void SSD1306_setCursor(unsigned char x, unsigned char y) {
    cursorX=x;cursorY=y;
    SSD1306_sendCommand(0xB0 + y);
    SSD1306_sendCommand(0x00 + (x & 0x0F));
    SSD1306_sendCommand(0x10 + ((x >> 4) & 0x0F));
}

void SSD1306_clear(void) {
    unsigned char page;
    unsigned char col;
    
    for (page = 0; page < 8; page++) {
        SSD1306_setCursor(0, page); // On force le passage à la page suivante
        SoftI2cMaster_start(0x78);
        SoftI2cMaster_write(0x40); // Mode Data
        for (col = 0; col < 128; col++) {
            SoftI2cMaster_write(0x00);
        }
        SoftI2cMaster_stop();
    }
    cursorX = 0; cursorY = 0;
}

void SSD1306_writeChar(char c) {
    unsigned char i;
    if (c >= 'a' && c <= 'z') c -= 32; // Vers Majuscule
    if (c < 32 || c > 90) c = 32;      // Hors table = Espace
    
    SoftI2cMaster_start(0x78);
    SoftI2cMaster_write(0x40);
    
    // MODIFICATION ICI : On commence à l'index 2 pour ignorer les colonnes vides
    // Et on ne prend que les 6 colonnes suivantes (de 2 à 7)
    for (i = 2; i < 8; i++) {
        SoftI2cMaster_write(font8x8[c - 32][i]);
    }
    
    // On ajoute un seul pixel d'espace au lieu de rien
    SoftI2cMaster_write(0x00); 
    SoftI2cMaster_stop();
}

void SSD1306_print(const char* text) {
    while (*text) {
        if (*text == '\n') {
            cursorX = 0; cursorY++;
            SSD1306_setCursor(cursorX, cursorY);
        } else {
            // MODIFICATION ICI : On considère que le caractère fait 6 pixels + 1 d'espace = 7
            if (cursorX > 121) { cursorX = 0; cursorY++; }
            SSD1306_setCursor(cursorX, cursorY);
            SSD1306_writeChar(*text);
            cursorX += 7; // On avance de 7 pixels au lieu de 8
        }
        text++;
    }
}
